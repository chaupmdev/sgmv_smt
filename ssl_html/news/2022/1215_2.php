						<p>　SGホールディングスグループにおいて、大型家具家電の設置輸送および移転を主軸に「輸送＋α」の付加価値を提供するSGムービング株式会社（本社：東京都江東区、代表取締役社長：角本高章、以下「SGムービング」）と、リネットジャパングループ株式会社の子会社で小型家電リサイクル事業を展開するリネットジャパンリサイクル株式会社（本社：愛知県大府市　代表取締役：中村俊夫、以下「リネットジャパン」）、北海道岩見沢市（市長：松野 哲）は連携協定を締結し、自治体公認の、家電リサイクル法指定4品目を含む使用済み家電を自宅から回収するサービスを開始しました。</p>
                        <br/>
                        <div class="photo"><img src="/news/common/img/20221215_01.png" alt="ロゴイメージ（岩見沢市）"></div>
                        <p>■連携協定締結の背景</p>
                        <p>　家電リサイクル法の指定4品目であるテレビ・冷蔵庫・洗濯機・エアコンと、小型家電リサイクル法の対象製品である電子レンジ・掃除機・空気清浄機・ドライヤーなどは、排出時に準拠する法律が異なることから、回収の手続きを別々に行う必要がある、家の中から指定場所への搬出が難しい、無許可業者により不適正に回収されている、といった課題があります。</p>
                        <p>　また、従来は一般消費者（排出者）が店頭で新しい製品を購入する際に使用済み製品の回収を依頼するという運用が一般的でしたが、昨今では通販サイトで家電を購入する方も多く、使用済み製品を処分するスキームが不足していました。その結果、自治体は処分方法の問い合わせや無許可業者による不適切な回収への対応などの負担が大きくなっています。</p>
                        <p>　さらに、昨今のロシア・ウクライナ情勢を契機として、使用済み家電のリサイクルを通じた希少金属の安定的な確保・調達の重要性も高まっており、環境省が2022年9月に公表した「循環経済工程表」でも2030年までに資源のリサイクル量を倍増させる計画が盛り込まれるなど、正しいリサイクルを一層進めていくことが求められています。</p>
                        <br/>
                        <p>　こうした課題へ対応するために３者は連携協定を締結し、SGムービングとリネットジャパンが提供する、自宅内からの搬出作業にも対応した使用済み家電の回収サービスを活用した取り組みを行うこととなりました。今後、岩見沢市が発行する各種媒体物を通じて使用済み家電の便利で正しい処分方法について住民の方への広報を行うなど、互いのリソースやノウハウを有効に活用して持続可能な暮らしやすいまちづくりと、社会課題の解決・改善に取り組んでまいります。</p>
                        <br/>

                        <p>■ サービス概要・利用方法</p>
                        <p>・ テレビ・冷蔵庫・洗濯機といった大型の製品から、レンジ・炊飯器・パソコン・ドライヤー・ラジオ・時計といった小型の製品まで、さまざまな家電の回収が可能です。</p>
                        <p>・ インターネット・電話から申込み後、収集運搬許可を保有する事業者が希望日時（年中無休・最短翌日）にご自宅へ回収に伺います。</p>
                        <p>・ 大型の家電製品は収集運搬業者が搬出するため、利用者が持ち出す必要はありません。</p>
                        <p>　リサイクル券の購入や配送伝票の記入・準備も不要です。</p>
                        <p>・ 利用料金は回収先住所・回収品目・メーカー・サイズ等によって異なります。詳細はWebサイトでご確認ください。</p>
                        <p>・ 回収後は、家電リサイクル法/小型家電リサイクル法のそれぞれの法律で定められている処理方法に則って正しく再資源化されます。</p>
