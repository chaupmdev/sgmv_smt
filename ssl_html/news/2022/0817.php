						<p><small>※「SG-ARK」に関する情報：<a href="/news/news-release/pdf/topic56.pdf" target="_black">https://www.sagawa-mov.co.jp/news/news-release/pdf/topic56.pdf</a></small></p>
						<p class="photo"><img src="/news/common/img/20220817-01.png" alt="ReNet">
					<p>リネットジャパンは環境省・経済産業省から小型家電リサイクル法に基づく許認可を取得し、SGホールディングスグループの佐川急便株式会社と連携し、使用済小型家電を宅配便で回収するリサイクルサービスを全国で展開しております。現在、約600自治体と協定等を締結、大手ネット通販・家電量販店、家電メーカー等とも提携し、小型家電の回収インフラとして規模を拡大しています。
						また、SGムービングは家電リサイクル法の指定4品目の回収に際し、小売業者と全国の収集運搬事業者をオンライン上で直接マッチングするサービスである「SG-ARK」を提供しております。</P>
						<p>使用済になった家電リサイクル法指定4品目と小型家電リサイクル法の対象製品は、それぞれ対応する法律が異なることから、排出時のルールを排出者が正確に理解していないことや、その煩雑さなどを背景に、無許可業者による不適正な回収によって再資源化されずに廃棄されていることが課題となっています。</p>
						<p>こうした課題に対処していくことを目的として、リネットジャパンが運営するパソコン・小型家電の回収サイト上において、「SG-ARK」の紹介を開始しました。</p>
						<p class="mb-30">この連携により、排出者にとって「家電リサイクル法指定4品目の正しい処分方法」の認知向上、また自治体にとっての住民サービス向上にも繋がります。
						今後、両社はさらなる連携強化を目指し、指定4品目と小型家電を「まとめて回収・リサイクルできる新たなサービス」を開発し、全国の自治体・小売業者等へサービスの提供を行うことで、排出者にとって簡便なリサイクルサービスの提供・リサイクル法を順守した回収促進を目指し取り組んでいきます。
						SGムービングとリネットジャパンは、再資源化を促進することによってSDGsへ貢献すると共に、持続可能な循環型社会の実現に向け、今後も積極的に活動してまいります。</p>
