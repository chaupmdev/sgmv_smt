							※SG-ARKについて：<a href="/service/pdf/SG-ARK.pdf" target="_blank">https://www.sagawa-mov.co.jp/service/pdf/SG-ARK.pdf</a>
							これまで家電リサイクル法で指定された家電4品目に限定した設置・回収業務のサポートを行ってまいりましたが、事業者が処理する事業系廃棄物全般を網羅的にサポートすることで、手配業務の簡素化を図ります。</P>
						<p class="headLine04 mt-30">SGムービングからのコメント</p>
						<p>当社は、SGホールディングスグループにおいて静脈物流の役割を担っています。「SGエコープ」の提供開始により、これまで「SG-ARK」において手がけてきた家電リサイクルや蓄電池回収などのサービスを連携強化し、動脈物流から静脈物流の流れを創り出すことにより、グループ全体で持続可能な循環型社会の実現およびSDGsの目標達成に貢献してまいります。</P>
