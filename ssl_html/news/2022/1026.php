                        <p class="text"></p>
						<p>　SGホールディングスグループにおいて、大型家具家電の設置輸送および移転を主軸に「輸送＋α」の付加価値を提供するSGムービング株式会社（本社：東京都江東区、代表取締役社長：角本高章、以下「SGムービング」）と、リネットジャパングループ株式会社の子会社で小型家電リサイクル事業を展開するリネットジャパンリサイクル株式会社（本社：愛知県大府市　代表取締役：中村俊夫、以下「リネットジャパン」）は業務提携を行い、小さな家電から大きな家電まで様々な使用済み家電を法律に基づいて正しく処分できる回収プラットフォームを協働で開設し、自治体のWebサイトや広報誌等での案内を通じて、排出者の利便性向上と家電リサイクルの促進に寄与するサービスの提供を開始しました。 </p>
                        <br/>
                        <div class="photo"><img src="/news/common/img/20221026_01.jpg" alt="Webサイトイメージ"></div>
                        <br/>
                        <p style="text-align: center; margin-bottom: 20px;;margin-top: -40px"">Webサイトイメージ</p>
                        <p>■ サービス開発の背景</p>
                        <p>　テレビ・冷蔵庫・洗濯機・エアコンの家電リサイクル法の指定4品目と、レンジ・掃除機・空気清浄機・ドライヤーなどの小型家電リサイクル法の対象製品は、排出時に準拠する法律が異なることから、回収の手続きを別々に行う必要がある、家の中から指定場所への搬出が難しい、無許可業者により不適正に回収されている、といった課題があります。</p>
                        <p>　また、従来は一般消費者（排出者）が店頭で新しい製品を購入する際に使用済み製品の回収をセットで依頼するという運用が一般的でしたが、昨今では通販サイトで家電を購入する方も多く、使用済み製品を処分するスキームが不足していました。その結果、自治体は処分方法の問い合わせ対応や無許可業者による不適正な回収への対応などの負担が大きくなっています。</p>
                        <p>　これまで、リネットジャパンは環境省・経済産業省から小型家電リサイクル法に基づく許認可を取得し、使用済小型家電を消費者から宅配便を活用して直接回収するリサイクルサービスを全国で展開しており、全国約600の自治体・大手ネット通販・大手家電量販店、家電メーカー等とも提携し、使用済小型家電の回収インフラとして規模を拡大してきました。</p>
                        <p>　また、SGムービングは家電リサイクル法の指定4品目の回収に際し、小売業者と全国の収集運搬業者をオンライン上で直接マッチングする「SG-ARK」を提供してきました。</p>
                        <br/>
                        <p>　この度、両社は業務提携し、使用済み家電の処分に関する課題解決や利便性向上を目的として本サービスを開発しました。今後は、本提携を通じて、家電リサイクル法・小型家電リサイクル法のそれぞれで求められる要件を網羅した運用を構築し、営業面でも連携して使用済み家電の回収インフラを拡大していきます。</p>
                        <br/>
                        <div class="photo"><img src="/news/common/img/20221026_02.jpg" alt="回収スキームイメージ"></div>
                        <br/>
                        <p style="text-align: center; margin-bottom: 20px;;margin-top: -40px"">回収スキームイメージ</p>
                        
                        <p>■ 本サービスの提供価値</p>
                        <p>① 消費者が使用済み家電を処分する時の利便性向上</p>
                        <p>　リサイクル法毎・サイズ毎に区別をすることなく、使用済み家電をまとめて処分できるようになります。最短翌日・希望日時に回収が可能で、家の中の設置場所からの搬出も行うため、大型の家電製品を処分する際も便利に利用できるようになります。</p>
                        <br/>
                        <p>② 法律に準拠した正しいリサイクルの推進</p>
                        <p>　家電リサイクル法・小型家電リサイクル法・廃棄物処理法に準拠した回収サービスです。回収後は各リサイクル法の認定工場に運ばれ、適切にリサイクル・再資源化されます。現状、約３割近くが無許可業者による回収とも言われており、本サービスの活用により正しいリサイクルを推進することに繋がります。</p>
                        <br/>
                        <p>③ 資源の有効利用／資源循環・回収量の増加</p>
                        <p>　テレビ・冷蔵庫・洗濯機などの家電４品目は家電リサイクル法により、小売業者で回収されることが前提となった義務法です。一方、小型家電は買い替えタイミングで回収する仕組み作りが十分でなく、粗大ごみとして埋め立て処理されてしまうケースもありました。本サービスの活用で、小型家電も家電４品目と同じように商品販売時に回収依頼を受け付けられるようになるため、回収量が増加すると考えています。</p>
                        <p>　昨今のロシア・ウクライナ情勢を契機として「資源の有効利用」と「国内での資源循環」の重要性が高まっています。家電製品の中には金・銀・レアメタル等が多く含まれていることから、環境省・経済産業省も家電製品の適切な回収・再資源化を推進していく方向性を示しており、今回のサービスはこうした流れにも合致するものと考えています。</p>
                        <br/>
                        <p>■ サービス概要・ご利用方法</p>
                        <p>・ テレビ・冷蔵庫・洗濯機といった大型の製品から、レンジ・炊飯器・パソコン・ドライヤー・ラジオ・時計といった小型の製品まで、大きさ問わず多くの家電製品が回収可能です。</p>
                        <p>・ インターネットから申し込み後、希望日時（年中無休・最短翌日）に法律に基づく正しい収集運搬許可を持っている事業者がご自宅へ回収に伺います。</p>
