                        <p class="title" style="text-align: left">スマホやPCから使用済み家電の回収申し込みが可能なプラットフォームの提供とWeb回収受付を開始</p>
                        <p style="text-align: left">～排出者の利便性向上と無許可業者等に回収されていた製品の適正化を目指し、自治体とも連携～</p>
