                        <p>１．締結経緯</p>
                        <p>賃貸管理会社様の困りごとのひとつに、特定家庭用機器再商品化法(家電リサイクル法)におけるエアコンのリサイクルがあります。本来、賃貸管理会社が家電小売業者として回収しなければいけないところ、複雑な法律と管理すべき事項の多さから適正な回収がなされてないことがあります。SGムービングでは、家電リサイクル回収業務をサポートする「SG-ARK」※を運営しており、賃貸管理物件から排出されるエアコンの適正な回収を推進するため、SGムービングと日管協にて協定を締結しました。</p>
                        <br>
                        <p>※「SG-ARK」は、SGホールディングス株式会社の登録商標です。</p>
                        <br>
                        <p>２．SG-ARKについて</p>
                        <p>「SG-ARK」は、Web上で家電リサイクル回収を依頼し、収集運搬業者の選定から契約締結までを完結できるサービスです。賃貸管理会社様にとっては、リサイクル券のステータス管理機能による処理状況の確認やスキャニング機能によるリサイクル券の電子保管、家電リサイクル料金支払い代行などの煩雑な事務作業が軽減でき、運用開始から現在まで取引実績は約13万件となっています。本協定を結ぶにあたり、賃貸管理会社様も利用しやすいよう、さらにシステム改良を行っています。</p>
